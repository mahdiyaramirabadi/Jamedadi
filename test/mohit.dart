import 'dart:math';

mohit() {
  dayere_mohit() {
    double dayere_mohit_r = 4;
    double dayere_mohit_P = dayere_mohit_r * 2 * pi;
    print('dayere_mohit');
    print(dayere_mohit_P);
  }

  mosalas_mohit() {
    double mosalas_mohit_a = 3;
    double mosalas_mohit_b = 3;
    double mosalas_mohit_c = 2;
    double mosalas_mohit_P =
        mosalas_mohit_a + mosalas_mohit_b + mosalas_mohit_c;
    print("mosalas_mohit");
    print(mosalas_mohit_P);
  }

  chandzeli_motesavialazla() {
    double chandzeli_motesavialazla_n = 3;
    double chandzeli_motesavialazla_a = 4;
    double chandzeli_motesavialazla_P =
        chandzeli_motesavialazla_a * chandzeli_motesavialazla_n;
    print('chandzeli_motesavialazla');
    print(chandzeli_motesavialazla_P);
  }

  chanzeli_motazam_mohit() {
    double chanzeli_motazam_mohit_n = 4;
    double chanzeli_motazam_mohit_b = 3;
    double chanzeli_motazam_mohit_P = 2 *
        chanzeli_motazam_mohit_n *
        chanzeli_motazam_mohit_b *
        sin(pi / chanzeli_motazam_mohit_n);
    print("chanzeli_motazam_mohit");
    print(chanzeli_motazam_mohit_P);
  }

  dayere_mohit();
  mosalas_mohit();
  chandzeli_motesavialazla();
  chanzeli_motazam_mohit();
}

main() {
  mohit();
}
